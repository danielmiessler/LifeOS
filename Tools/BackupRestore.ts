#!/usr/bin/env bun
/**
 * BackupRestore - Backup, restore, and upgrade PAI installations
 *
 * Commands:
 *   backup [--name <label>]                          - Create timestamped backup of ~/.claude
 *   backup --user-only --release <path>              - Back up only files that differ from release
 *   restore <backup-name>                            - Restore from backup
 *   list                                             - List available backups
 *   migrate <backup>                                 - Analyze backup for migration candidates
 *   diff --release <path>                            - Compare local install against a release
 *   upgrade --release <path>                         - Safe upgrade with automatic backup/restore
 *
 * Usage:
 *   bun BackupRestore.ts backup
 *   bun BackupRestore.ts backup --name "before-upgrade"
 *   bun BackupRestore.ts backup --user-only --release ./Releases/v4.1.0/.claude
 *   bun BackupRestore.ts list
 *   bun BackupRestore.ts restore claude-backup-20260114-153000
 *   bun BackupRestore.ts migrate claude-backup-20260114-153000
 *   bun BackupRestore.ts diff --release ./Releases/v4.1.0/.claude
 *   bun BackupRestore.ts upgrade --release ./Releases/v4.1.0/.claude
 */

import {
  existsSync,
  readdirSync,
  statSync,
  readFileSync,
  cpSync,
  rmSync,
  mkdirSync,
  writeFileSync,
} from "fs";
import { homedir } from "os";
import { join, basename, relative, dirname } from "path";
import { createHash } from "crypto";
import { execSync } from "child_process";

const HOME = homedir();
const CLAUDE_DIR = join(HOME, ".claude");
const BACKUP_PREFIX = "claude-backup-";
const USER_BACKUP_PREFIX = "claude-user-backup-";

// Files that should not be restored during upgrade because they are regenerated:
// - CLAUDE.md: regenerated by BuildCLAUDE.ts from template + settings.json
//
// Note: settings.json IS restored before install.sh runs, so that install.sh's
// merge logic reads the user's existing config (hooks, statusLine, etc.) and
// only updates version/identity fields.
const UPGRADE_SKIP_RESTORE = new Set(["CLAUDE.md"]);

// Claude Code runtime directories with no user value — excluded from user-only backup.
// These are machine-specific, session-specific, or auto-regenerated.
const CC_RUNTIME_EXCLUDE = new Set([
  "debug",
  "todos",
  "session-env",
  "sessions",
  "shell-snapshots",
  "paste-cache",
  "cache",
  "image-cache",
  "telemetry",
  "backups",
  "claude-history-md",
]);

/**
 * Parse a .gitignore file and return a list of patterns.
 * Supports simple filename matches (e.g., ".DS_Store", "*.log")
 * and directory patterns (e.g., "node_modules/").
 * Does not support negation (!) or complex glob patterns.
 */
function parseGitignore(gitignorePath: string): string[] {
  if (!existsSync(gitignorePath)) return [];
  try {
    return readFileSync(gitignorePath, "utf-8")
      .split("\n")
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith("#"));
  } catch {
    return [];
  }
}

/**
 * Check if a relative file path matches any gitignore pattern.
 */
function isIgnored(relPath: string, patterns: string[]): boolean {
  const fileName = basename(relPath);
  const parts = relPath.split("/");

  for (const pattern of patterns) {
    // Directory pattern: "node_modules/" matches any path segment
    if (pattern.endsWith("/")) {
      const dirName = pattern.slice(0, -1);
      if (parts.includes(dirName)) return true;
      continue;
    }

    // Wildcard pattern: "*.log" matches file extension
    if (pattern.startsWith("*.")) {
      const ext = pattern.slice(1); // ".log"
      if (fileName.endsWith(ext)) return true;
      continue;
    }

    // Prefix pattern: "._*" matches filenames starting with prefix
    if (pattern.endsWith("*")) {
      const prefix = pattern.slice(0, -1);
      if (fileName.startsWith(prefix)) return true;
      continue;
    }

    // Exact filename match: ".DS_Store"
    if (fileName === pattern) return true;

    // Path match: "private/" or ".claude/pai_updates/"
    if (relPath === pattern || relPath.startsWith(pattern + "/")) return true;
  }

  return false;
}

/**
 * Find and load .gitignore from the repo root (parent of release dir).
 * Walks up from the release directory looking for .gitignore.
 */
function loadIgnorePatterns(releaseDir: string): string[] {
  // Walk up to find repo root .gitignore
  let dir = releaseDir;
  for (let i = 0; i < 5; i++) {
    const gitignorePath = join(dir, ".gitignore");
    if (existsSync(gitignorePath)) {
      return parseGitignore(gitignorePath);
    }
    const parent = dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  // Fallback: minimal ignore list
  return [".DS_Store", "Thumbs.db"];
}

