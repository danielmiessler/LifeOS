interface BackupInfo {
  name: string;
  path: string;
  date: Date;
  size: string;
  hasSettings: boolean;
  hasHooks: boolean;
  hasSkills: boolean;
}

interface MigrationCandidate {
  type: "settings" | "hook" | "skill" | "memory";
  path: string;
  description: string;
}

interface DiffResult {
  newFiles: string[]; // In release but not in local
  unchanged: string[]; // In both, same hash
  conflicts: string[]; // In both, different hash
  releaseDir: string;
}

